package task3.model;

public class Values {
    private final double a;
    private final double b;
    private final double h;

    public Values(double a, double b, double h)
    {
        this.a = a;
        this.b = b;
        this.h = h;
    }

    public double getA() { return a; }
    public double getB() { return b; }
    public double getH() { return h; }
}
